        Assert.Contains("<CALL:4>W1AW", adif);
        Assert.Contains("<STATION_CALLSIGN:5>K7RND", adif);
        Assert.Contains("<BAND:3>20M", adif);
        Assert.Contains("<MODE:3>FT8", adif);
        Assert.Contains("<QSO_DATE:8>20240615", adif);
        Assert.Contains("<TIME_ON:6>183000", adif);
        Assert.Contains("<eor>", adif);
