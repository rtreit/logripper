    [Fact]
    public void Parse_valid_adif_with_header()
    {
        const string adif = """
            Generated by test
            <ADIF_VER:5>3.1.7
            <EOH>
            <CALL:4>W1AW<BAND:3>20M<MODE:3>SSB<QSO_DATE:8>20240101<TIME_ON:6>123456<eor>
            """;

        var qsos = AdifCodec.ParseAdif(adif);

        Assert.Single(qsos);
        Assert.Equal("W1AW", qsos[0].WorkedCallsign);
        Assert.Equal(Band._20M, qsos[0].Band);
        Assert.Equal(Mode.Ssb, qsos[0].Mode);
        Assert.NotNull(qsos[0].UtcTimestamp);
        Assert.Equal(2024, qsos[0].UtcTimestamp.ToDateTimeOffset().Year);
        Assert.Equal(12, qsos[0].UtcTimestamp.ToDateTimeOffset().Hour);
        Assert.Equal(34, qsos[0].UtcTimestamp.ToDateTimeOffset().Minute);
        Assert.Equal(56, qsos[0].UtcTimestamp.ToDateTimeOffset().Second);
    }

    [Fact]
    public void Parse_skips_header_fields()
    {
        const string adif = "<ADIF_VER:5>3.1.7<PROGRAMID:9>QsoRipper<EOH><CALL:4>W1AW<BAND:3>20M<MODE:2>CW<QSO_DATE:8>20240601<TIME_ON:4>0800<eor>";

        var qsos = AdifCodec.ParseAdif(adif);

        Assert.Single(qsos);
        Assert.Equal("W1AW", qsos[0].WorkedCallsign);
        Assert.DoesNotContain("ADIF_VER", (IDictionary<string, string>)qsos[0].ExtraFields);
        Assert.DoesNotContain("PROGRAMID", (IDictionary<string, string>)qsos[0].ExtraFields);
    }

    [Fact]
